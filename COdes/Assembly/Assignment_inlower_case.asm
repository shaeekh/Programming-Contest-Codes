.MODEL SMALL
.STACK 100H
.DATA
CR EQU 0DH
LF EQU 0AH
M1 DB 'ENTER AN UPPERCASE LETTER:$'
M2 DB 0DH,0AH, 'IN LOWERCASE IT IS:'
CAH DB ?,'$'

.CODE
MAIN PROC
    MOV AX,@DATA
    MOV DS,AX
    
    LEA DX,M1
    MOV AH,9
    INT 21H
    
    MOV AH,1
    INT 21H
    ADD AL,20H
    MOV CAH,AL
    
    LEA DX,M2
    MOV AH,9
    INT 21H
    
    MOV AH,4CH
    INT 21H
    END MAIN